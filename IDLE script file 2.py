# Define the greaterThan function
def greaterThan(x, y):
    return x > y

# Assign values to variables
a = 2
b = 3

# Call the function and store the result
result = greaterThan(a, b)

# Print the output statement
print("The statement", str(a), "is greater than", str(b), "is", str(result))

# Testing the second scenario
a = 10
b = 6

# Call the function again and store the result
result = greaterThan(a, b)

# Print the new output statement
print("The statement", str(a), "is greater than", str(b), "is", str(result))
