# This script prints the sentence "It is raining cats and dogs"
# Variables to store the words "cats" and "dogs"
c = "cats"  # Set variable 'c' to "cats"
d = "dogs"  # Set variable 'd' to "dogs"

# Constructing the sentence with variables
s = "It is raining " + c + " and " + d + "."  # Set 's' to the sentence using concatenation

# Printing the sentence
print(s)  # Output the sentence
